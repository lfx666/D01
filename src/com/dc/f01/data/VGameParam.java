public class VGameParam {
    private String robotSwitch;

    private String startTime;

    private String endTime;

    private String minInterval;

    private String maxInterval;

    public String getRobotSwitch() {
        return robotSwitch;
    }

    public void setRobotSwitch(String robotSwitch) {
        this.robotSwitch = robotSwitch;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public String getMinInterval() {
        return minInterval;
    }

    public void setMinInterval(String minInterval) {
        this.minInterval = minInterval;
    }

    public String getMaxInterval() {
        return maxInterval;
    }

    public void setMaxInterval(String maxInterval) {
        this.maxInterval = maxInterval;
    }
}
